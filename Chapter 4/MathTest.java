import java.lang.Math;

public class MathTest {
   public static void main(String[] a) {
      System.out.println("SQRT: " + Math.sqrt(25));
      System.out.println("SIN: " + Math.sin(33));
      System.out.println("COS: " + Math.cos(40));
      System.out.println("FLOOR: " + Math.floor(25));
      System.out.println("CEIL: " + Math.ceil(30));
      System.out.println("ROUND: " + Math.round(25));
      System.out.println("MAX: " + Math.max(25, 30));
      System.out.println("MIN: " + Math.min(25, 28));
      System.out.println("RANDOM: " + Math.random());
   }
}